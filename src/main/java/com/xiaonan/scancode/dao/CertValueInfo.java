package com.xiaonan.scancode.dao;

public class CertValueInfo {
	private String outerItemId;
	private String outerCertCardValue;

	public String getOuterItemId() {
		return outerItemId;
	}

	public void setOuterItemId(String outerItemId) {
		this.outerItemId = outerItemId;
	}

	public String getOuterCertCardValue() {
		return outerCertCardValue;
	}

	public void setOuterCertCardValue(String outerCertCardValue) {
		this.outerCertCardValue = outerCertCardValue;
	}
}
